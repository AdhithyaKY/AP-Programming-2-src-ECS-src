public class ExcapeSequencePractice {

	public static void main(String[] args) {
		
		// \t is an escape sequence character to tab output to the right 5 spaces
		
		System.out.println("\t\t\tWelcome to Computer Science!");
		System.out.println("\t\t\t****************************\n\n");
		System.out.println();
		System.out.println();
		// \\ is an escape sequence to display one \
		System.out.println("\\\\\\\\*////");
		System.out.println("Mary had a \nlittle lamb\nwe are done");
		System.out.println("#///\"%\"\\\\\\#");
	}

}
